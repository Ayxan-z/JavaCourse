package homework2;

public class LoopDemo {

	public static void main(String[] args) {
		//For
		for (int i=0; i<10; i++) {
			System.out.println(i);
		}

		//While
		int i = 0;
		while (i<10) {
			System.out.println(i);
			i++;
		}

		//Do-While
		int j = 0;
		do {
			System.out.println(j);
			j++;
		}while(j<10);
	}

}
