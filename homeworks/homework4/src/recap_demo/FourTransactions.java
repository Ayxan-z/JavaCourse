package recap_demo;

public class FourTransactions {
    public int Sum(int n1, int n2) {
        return n1 + n2;
    }

    public int Substraction(int n1, int n2) {
        return n1 - n2;
    }

    public int Multiplication(int n1, int n2) {
        return n1 * n2;
    }

    public int Divide(int n1, int n2) {
        return n1 / n2;
    }
}
